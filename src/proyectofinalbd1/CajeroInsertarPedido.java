     private void MostrarCarrito(){
         
         double total=0; 
    try {
        
        DefaultTableModel modelo=new DefaultTableModel();
        
            modelo.addColumn("idCarrito");
            modelo.addColumn("Nombre del Producto");
            modelo.addColumn("Cantidad del Producto");
            modelo.addColumn("Precio (C/U)");
            modelo.addColumn("Precio Total");
                      
          conexion ob = new conexion();
          cn=ob.con();
          st=cn.createStatement();
          rs=st.executeQuery("call SP_MostrarCarrito()");
         // System.out.println(rs);
          datos=new String[5];
          while(rs.next()){
          datos[0]=rs.getString(1);
          datos[1]=rs.getString(2);
          datos[2]=rs.getString(3);
          datos[3]=rs.getString(4);
          datos[4]=rs.getString(5);
          total+=Double.parseDouble(datos[4]);
          modelo.addRow(datos);
          }
          datos[0]="";
          datos[1]="";
          datos[2]="";
          datos[3]="";
          datos[4]="";
          modelo.addRow(datos);
          datos[0]="Total ";
          datos[1]="";
          datos[2]="";
          datos[3]="";
          datos[4]=String.valueOf(total);
          modelo.addRow(datos);
          T_Mostrar.setModel(modelo);
        
          cn.close();
          st.close();
          rs.close();
    } catch (SQLException ex) {
       // Logger.getLogger(AdministradorInsertarProducto.class.getName()).log(Level.SEVERE, null, ex);
         System.out.println("No se cargaron los datos, en el SP_MostrarProducto");
    }
    
    }
