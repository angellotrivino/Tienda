    private void B_InsertarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_InsertarPedidoActionPerformed
        // TODO add your handling code here:
        if(CB_NombreProducto.getSelectedIndex()==0  || TF_CantidadProducto.getText().equals(Empty) ){
            JOptionPane.showMessageDialog(null,"Campos vacios","Â¿?",JOptionPane.INFORMATION_MESSAGE);
        }else
        {
            ////producto existente
            try {

                conexion ob = new conexion();
                cn=ob.con();
                st=cn.createStatement();
                rs=st.executeQuery("call SP_ProductoExistente('"+CB_String+"')");
                
                
                while(rs.next()){
                if(rs.getString(1).equals(CB_String)){
                 JOptionPane.showMessageDialog(null,"Ese Producto ya existe en el carro si quiere modificar"
                         + " la cantidad. De le Click en Archivo, Modificar Pedido","YA EXISTE!",JOptionPane.INFORMATION_MESSAGE);
                 return;
                }
                }
               
                cn.close();
                st.close();
                rs.close();
            } catch (SQLException ex) {
                // Logger.getLogger(AdministradorInsertarProducto.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("No se cargaron los datos, en el SP_InsertarProducto");
                
            }
            
            /////insertar al carrito
            try {

                conexion ob = new conexion();
                cn=ob.con();
                st=cn.createStatement();
                rs=st.executeQuery("call SP_InsertarCarrito('"+CB_String+"',"+Integer.parseInt(TF_CantidadProducto.getText())+")");

                CB_NombreProducto.setSelectedIndex(0);
                TF_CantidadProducto.setText(Empty);
                

                JOptionPane.showMessageDialog(null,"Se ha cargado correctamente","CORRECTO!",JOptionPane.INFORMATION_MESSAGE);
                MostrarCarrito();
                cn.close();
                st.close();
                rs.close();
            } catch (SQLException ex) {
                // Logger.getLogger(AdministradorInsertarProducto.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("No se cargaron los datos, en el SP_InsertarProducto");
                JOptionPane.showMessageDialog(null,"Ese Producto YA existe","VUELVA A INTENTARLO!",JOptionPane.WARNING_MESSAGE);
            }

        }
    }//GEN-LAST:event_B_InsertarPedidoActionPerformed

