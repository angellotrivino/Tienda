     private void MostrarUsuario(){
        
    try {
        
        DefaultTableModel modelo=new DefaultTableModel();
        
            modelo.addColumn("Codigo");
            modelo.addColumn("Nombres");
            modelo.addColumn("Apellidos");
            modelo.addColumn("Edad");
            modelo.addColumn("correo");
            modelo.addColumn("Loggin");
            modelo.addColumn("Contraseña");
            modelo.addColumn("Rol");
          
          conexion ob = new conexion();
          cn=ob.con();
          st=cn.createStatement();
          rs=st.executeQuery("call SP_MostrarUsuario()");
         // System.out.println(rs);
          datos=new String[8];
          while(rs.next()){
          datos[0]=rs.getString(1);
          datos[1]=rs.getString(2);
          datos[2]=rs.getString(3);
          datos[3]=rs.getString(4);
          datos[4]=rs.getString(5);
          datos[5]=rs.getString(6);
          datos[6]=rs.getString(7);
          datos[7]=rs.getString(8);
          if(datos[7].equals("1"))
              datos[7]="ADMINISTRADOR";
          else
              datos[7]="CAJERO";
          
          modelo.addRow(datos);
          }
        
        T_Mostrar.setModel(modelo);
        
          cn.close();
          st.close();
          rs.close();
    } catch (SQLException ex) {
       // Logger.getLogger(AdministradorInsertarProducto.class.getName()).log(Level.SEVERE, null, ex);
         System.out.println("No se cargaron los datos, en el SP_MostrarProducto");
    }
    
    }

