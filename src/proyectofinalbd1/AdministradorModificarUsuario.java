    private void B_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_ModificarActionPerformed
        // TODO add your handling code here:
        int Rol=0;
        if(TF_NombresUsuario.getText().equals(Empty) || TF_ApellidosUsuario.getText().equals(Empty) ||
            TF_EdadUsuario.getText().equals(Empty) || TF_CorreoUsuario.getText().equals(Empty) || 
            TF_LogginUsuario.getText().equals(Empty) || TF_PasswordUsuario.getText().equals(Empty) || CB_TipoUsuario.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Campos vacios","¿?",JOptionPane.INFORMATION_MESSAGE);
        }else
        {
            try {
                if(CB_TipoUsuario.getSelectedIndex()==1)
                    Rol=1;
                else
                    if(CB_TipoUsuario.getSelectedIndex()==2)
                        Rol=2;
                
                conexion ob = new conexion();
                cn=ob.con();
                st=cn.createStatement();
                rs=st.executeQuery("call SP_ModificarUsuario("+Integer.parseInt(TF_CodigoUsuario.getText())+",'"
                    +TF_NombresUsuario.getText()+"','"+TF_ApellidosUsuario.getText()+"',"+Integer.parseInt(TF_EdadUsuario.getText())
                    +",'"+TF_CorreoUsuario.getText()+"','"+TF_LogginUsuario.getText()+"','"+TF_PasswordUsuario.getText()+"',"+Rol+")");
                   System.out.println(rs.toString());
                if(rs.toString().equals("Result set representing update count of 0")){
                JOptionPane.showMessageDialog(null,"Ese Codigo no existe","INCORRECTO!",JOptionPane.WARNING_MESSAGE);
                }else{

                   

                JOptionPane.showMessageDialog(null,"Se ha Modificado correctamente","CORRECTO!",JOptionPane.INFORMATION_MESSAGE);
                MostrarUsuario();
                }
                cn.close();
                st.close();
                rs.close();
            } catch (SQLException ex) {
                // Logger.getLogger(AdministradorInsertarProducto.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("No se cargaron los datos, en el SP_ModificarUsuario");
                }
             B_BorrarCamposActionPerformed(null);

        }
    }//GEN-LAST:event_B_ModificarActionPerformed

