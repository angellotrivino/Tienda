    private void TF_IdCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TF_IdCarritoActionPerformed
        // TODO add your handling code here:

        try {

            conexion ob = new conexion();
            cn=ob.con();
            st=cn.createStatement();
            rs=st.executeQuery("call SP_MostrarCarrito()");
            // System.out.println(rs);

            while(rs.next()){
                if(TF_IdCarrito.getText().equals(rs.getString(1))){
                    TF_CantidadProducto.setText(rs.getString(3));
                    L4.setText(rs.getString(2));
                    foto = new ImageIcon(Ruta+rs.getString(6));
                    L3.setIcon(new ImageIcon(foto.getImage().getScaledInstance(100,100,Image.SCALE_SMOOTH)));
                    break;
                }else
                if(!TF_IdCarrito.getText().equals(rs.getString(1))){

                    TF_CantidadProducto.setText(Empty);
                    L4.setText(Empty);
                    L3.setIcon(null);

                }
            }

            cn.close();
            st.close();
            rs.close();
        } catch (SQLException ex) {
            // Logger.getLogger(AdministradorInsertarProducto.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("No se cargaron los datos, en el SP_MostrarProducto");
        }
    }//GEN-LAST:event_TF_IdCarritoActionPerformed

