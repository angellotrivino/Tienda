    private void B_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_GuardarActionPerformed
        // TODO add your handling code here:
         if(TF_CodigoProducto.getText().equals(Empty) || TF_NombreProducto.getText().equals(Empty) || 
                 TF_PrecioProducto.getText().equals(Empty) || TF_Examinar.getText().equals(Empty)){
                  JOptionPane.showMessageDialog(null,"Campos vacios","¿?",JOptionPane.INFORMATION_MESSAGE);                
          }else
                {
             try {
                 
                 conexion ob = new conexion();
                 cn=ob.con();
                 st=cn.createStatement();
                 rs=st.executeQuery("call SP_InsertarProducto("+Integer.parseInt(TF_CodigoProducto.getText())+",'"
                         +TF_NombreProducto.getText()+"',"+Double.parseDouble(TF_PrecioProducto.getText())+",'"+TF_Examinar.getText()+"')");
                 
                  TF_CodigoProducto.setText(Empty);
                  TF_NombreProducto.setText(Empty);
                  TF_PrecioProducto.setText(Empty);
                  TF_Examinar.setText(Empty);
                  TF_CodigoProducto.requestFocus();
                  
                  JOptionPane.showMessageDialog(null,"Se ha cargado correctamente","CORRECTO!",JOptionPane.INFORMATION_MESSAGE);
                  MostrarProducto();
                   cn.close();
                   st.close();
                   rs.close();
             } catch (SQLException ex) {
                // Logger.getLogger(AdministradorInsertarProducto.class.getName()).log(Level.SEVERE, null, ex);
                 System.out.println("No se cargaron los datos, en el SP_InsertarProducto");
                 JOptionPane.showMessageDialog(null,"Ese codigo de Producto YA existe","VUELVA A INTENTARLO!",JOptionPane.WARNING_MESSAGE);
             }
                
                }
    }//GEN-LAST:event_B_GuardarActionPerformed

