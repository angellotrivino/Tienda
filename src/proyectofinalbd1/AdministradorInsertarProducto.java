    private void B_ExaminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_ExaminarActionPerformed
        // TODO add your handling code here:
        
        final JFrame vn = new JFrame("Abrir");
        vn.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        
        final JFileChooser FC_Examinar = new JFileChooser();
        
        FC_Examinar.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent ae) {
               String command=ae.getActionCommand();
        if(command.equals(JFileChooser.APPROVE_SELECTION)){
        File archivoSeleccionado =FC_Examinar.getSelectedFile();
        TF_Examinar.setText(archivoSeleccionado.getName());
        vn.dispose();
        }else
            if(command.equals(JFileChooser.CANCEL_SELECTION)){
            vn.dispose();
            }
            }
        });
        vn.add(FC_Examinar,BorderLayout.CENTER);
        vn.pack();
        vn.setVisible(true);
    }//GEN-LAST:event_B_ExaminarActionPerformed


